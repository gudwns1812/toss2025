package lang.string;

public class CharArrayMain {

    public static void main(String[] args) {
        char[] charArray = new char[]{
                'H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd'
        };
        System.out.println(charArray);
        System.out.println("charArray = " + charArray);

        String str = "hello world";
        System.out.println("str = " + str);
    }
}
